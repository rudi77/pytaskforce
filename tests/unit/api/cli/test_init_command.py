class TestSetupWorkspace:
    """Tests for workspace directory setup."""

    def test_creates_workspace_directory(self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        monkeypatch.chdir(tmp_path)
        config = InitConfig(
            provider="openai",
            api_keys={"OPENAI_API_KEY": "sk-test"},
            model_aliases={"main": "gpt-4.1-mini", "powerful": "gpt-4.1", "fast": "gpt-4.1-nano"},
            profile="dev",
            work_dir=Path(".taskforce"),
        )

        _setup_workspace(config)

        assert (tmp_path / ".taskforce" / "config").is_dir()
        assert (tmp_path / ".env").is_file()
        assert (tmp_path / ".taskforce" / "config" / "llm_config.yaml").is_file()
        assert (tmp_path / ".taskforce" / "config" / "profile.yaml").is_file()

    def test_preserves_existing_env(self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        monkeypatch.chdir(tmp_path)
        env_file = tmp_path / ".env"
        env_file.write_text("EXISTING=value\n")

        config = InitConfig(
            provider="openai",
            api_keys={"OPENAI_API_KEY": "sk-new"},
            model_aliases={"main": "gpt-4.1-mini"},
            work_dir=Path(".taskforce"),
        )

        # Mock typer.confirm to return False (don't overwrite)
        with patch("taskforce.api.cli.commands.init.typer.confirm", return_value=False):
            _setup_workspace(config)

        # Original content preserved
        assert env_file.read_text() == "EXISTING=value\n"

    def test_adds_env_to_gitignore(self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        monkeypatch.chdir(tmp_path)
        gitignore = tmp_path / ".gitignore"
        gitignore.write_text("*.pyc\n")

        config = InitConfig(
            provider="openai",
            api_keys={},
            model_aliases={"main": "gpt-4.1-mini"},
            work_dir=Path(".taskforce"),
        )

        _setup_workspace(config)

        content = gitignore.read_text()
        assert ".env" in content
